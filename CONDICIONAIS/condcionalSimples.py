# O que é uma condicional simples?
# É uma estrutura de controle que permite executar um bloco de código apenas se uma condição específica for verdadeira.
# Sintaxe básica:
# if condição:
#     bloco de código a ser executado se a condição for verdadeira
# Exemplo prático:
idade = int(input("Digite sua idade: "))
if idade >= 18:
    print("Você é maior de idade.")
    print(f"Pode votar e dirigir com {idade} anos.")