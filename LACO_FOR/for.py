# O que é laço for?
# É uma estrutura de controle que permite iterar sobre uma sequência (como listas, tuplas, dicionários, conjuntos ou strings) ou qualquer outro objeto iterável.
# Sintaxe básica:
# for item in sequencia:
#     bloco de código a ser executado para cada item na sequência
# Exemplo prático:
for numero in range(10):  # Itera de 0 a 9
    print(f"Número atual: {numero}")