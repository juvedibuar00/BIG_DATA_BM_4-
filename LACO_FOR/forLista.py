# For em uma lista
# O que é laço for em uma lista?
# É uma estrutura de controle que permite iterar sobre os elementos de uma lista, executando um bloco de código para cada elemento.
# Sintaxe básica:
# for item in lista:
#     bloco de código a ser executado para cada item na lista
# Exemplo prático:
minha_lista = [10, 20, 30, 40, 50]
for elemento in minha_lista:
    print(f"Elemento atual da lista: {elemento}")